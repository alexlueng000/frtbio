    toggle.firstChild && (toggle.firstChild.nodeValue = (lang === 'en' ? 'English' : '中文') + ' ');
  }

  // 暴露一个获取当前语言的函数，给下面的 i18n loader 用
  window.__getLang = getLang;
})();
